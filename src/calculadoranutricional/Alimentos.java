package calculadoranutricional;


public class Alimentos {
   
   private int id;
   private String nome;
   private int calorias;
   private int proteina;
   private int carboidrato;
   private int lipideo;
   private int ferro;
   private int sodio;
   private int colesterol;
   private int fibra;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
   
   

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCalorias() {
        return calorias;
    }

    public void setCalorias(int calorias) {
        this.calorias = calorias;
    }

    public int getProteina() {
        return proteina;
    }

    public void setProteina(int proteina) {
        this.proteina = proteina;
    }

    public int getCarboidrato() {
        return carboidrato;
    }

    public void setCarboidrato(int carboidrato) {
        this.carboidrato = carboidrato;
    }

    public int getLipideo() {
        return lipideo;
    }

    public void setLipideo(int lipideo) {
        this.lipideo = lipideo;
    }

    public int getFerro() {
        return ferro;
    }

    public void setFerro(int ferro) {
        this.ferro = ferro;
    }

    public int getSodio() {
        return sodio;
    }

    public void setSodio(int sodio) {
        this.sodio = sodio;
    }

    public int getColesterol() {
        return colesterol;
    }

    public void setColesterol(int colesterol) {
        this.colesterol = colesterol;
    }

    public int getFibra() {
        return fibra;
    }

    public void setFibra(int fibra) {
        this.fibra = fibra;
    }

   
  
}
